/**
 * Get a browse nudge based on user behavior
 */
export function getBrowseNudge(pagesViewed: number, viewedExample: boolean): string | null {
  if (viewedExample) {
    return strangerCopy.browseNudges.afterExample;
  }
  if (pagesViewed >= 3) {
    return strangerCopy.browseNudges.afterPages;
  }
  if (pagesViewed >= 1) {
    return strangerCopy.browseNudges.initial;
  }
  return null;
}

/**
 * Get trust signal for display
 */
export function getRandomTrustSignal(): string {
  const signals = Object.values(strangerCopy.trust);
  return signals[Math.floor(Math.random() * signals.length)];
}
