export type LegTransportType = 'flight' | 'train' | 'bus' | 'car' | 'ferry';

export interface FlightLegSlot {
  /** Unique identifier for this slot */
  id: string;
  /** Index of the city transition this slot belongs to (-1 for custom legs) */
  transitionIndex: number;
  /** Label shown to the user, e.g. "London → Paris" */
  label: string;
  /** Whether this is an outbound, inter-city, or return leg */
  legType: 'outbound' | 'intercity' | 'return' | 'custom';
  /** Transport type for this slot */
  transportType: LegTransportType;
  /** The flight data for this slot */
  flight: ManualFlightEntry;
  /** Pre-filled from/to based on city airports */
  suggestedFrom?: string;
  suggestedTo?: string;
  /** Whether this slot was auto-generated or manually added */
  isAutoGenerated: boolean;
  /** Whether details section is expanded */
  isExpanded: boolean;
  /** Additional connecting flights grouped with this slot */
  connectionLegs?: ManualFlightEntry[];
}

interface MultiLegFlightEditorProps {
  destinations: TripDestination[];
  startDate: string;
  endDate: string;
  /** Transport selections from InterCityTransportComparison (transition index → type) */
  transportSelections?: Record<number, { type: LegTransportType }>;
  /** Called whenever legs change */
  onLegsChange: (legs: ManualFlightEntry[]) => void;
  /** Called when user changes transport mode for an intercity leg */
  onTransportModeChange?: (transitionIndex: number, mode: LegTransportType) => void;
  /** Called to open the import modal */
  onOpenImport: () => void;
  /** Current outbound/return/additional legs from parent (for init) */
  initialOutbound?: ManualFlightEntry;
  initialReturn?: ManualFlightEntry;
  initialAdditionalLegs?: ManualFlightEntry[];
  /** Explicitly imported legs from modal; applied by index to visible slots */
  importedLegs?: ManualFlightEntry[];
  /** Incremented on each import to force deterministic re-apply */
  importNonce?: number;
}

