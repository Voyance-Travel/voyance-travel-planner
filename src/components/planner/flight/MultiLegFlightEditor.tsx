/**
 * MultiLegFlightEditor
 * 
 * Auto-generates flight leg input slots based on multi-city destinations.
 * For a trip ATL → London → Paris → ATL, generates:
 *   Leg 1: ATL → LHR (outbound)
 *   Leg 2: LHR → CDG (inter-city)
 *   Leg 3: CDG → ATL (return)
 * 
 * Also supports adding custom legs (e.g. connecting flights).
 * For single-city trips, falls back to outbound + optional return.
 */

